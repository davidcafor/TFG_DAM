    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //BOTON GUARDAR CONFIGURACION
        String ip = txtIP.getText();
        String puerto = txtPuerto.getText();

        Properties properties = new Properties();

        properties.setProperty("URL", ip);
        properties.setProperty("PUERTO", puerto);
        
        //PROPIEDADES NO MODIFICABLES
        properties.setProperty("USUARIO", "desktop");
        properties.setProperty("CLAVE", "abc123.");
        properties.setProperty("BD", "modus");
        properties.setProperty("FTPFOLDER", "/imagesApp/");
        properties.setProperty("USUARIOFTP", "ftp");
        properties.setProperty("CLAVEFTP", "abc123.");
        properties.setProperty("PUERTOFTP", "21");

        String fileName = "configuracion.properties";
        String folderPath = "Configuraciones";
        String filePath;

        if (Configuraciones.ConfiguracionEjecucion.SISTEMA_EN_PRODUCCION) {
            System.out.println("Producción");
            filePath = "." + File.separator + fileName;
            System.out.println(filePath);

        } else {
            System.out.println("Desarrollo");
            filePath = folderPath + File.separator + fileName;
            System.out.println(filePath);
        }

        /*File folder = new File(folderPath);
        if (!folder.exists()) {
            folder.mkdirs();
        }*/

        //Una vez creado archivo, escribo propiedades
        try ( OutputStream output = new FileOutputStream(filePath)) {
            properties.store(output, "Configuración de la aplicación");
        } catch (IOException ex) {
            ex.printStackTrace();
        }
       
        parentFrame.conectarBD();
        
        dispose();

    }//GEN-LAST:event_jButton1ActionPerformed

